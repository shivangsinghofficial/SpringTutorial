package com.shivangSingh.SpringBootTutorial.Domain;

public class EmployeeCoreJava {
    private String id;
    private String teamId;
    private String name;
    private String phoneNumber;

    public EmployeeCoreJava(String id, String teamId, String name, String phoneNumber) {
        this.id = id;
        this.teamId = teamId;
        this.name = name;
        this.phoneNumber = phoneNumber;
    }

    // Getter methods
    public String getId() {
        return this.id;
    }

    public String getTeamId() {
        return this.id;
    }

    public String getPhoneNumber() {
        return this.id;
    }

    public String getName() {
        return this.id;
    }

    // Setter methods
    public void setId(String id) {
        this.id = id;
    }

    public void setName (String name) {
        this.name = name;
    }
}




